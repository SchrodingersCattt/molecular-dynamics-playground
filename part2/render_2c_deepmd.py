    # -----------------------------------------------------------------------
    # Main animate callback
    # -----------------------------------------------------------------------
    def animate(self, frame_idx):
        step_idx, sub_frame = self.frame_schedule[frame_idx]
        d = self.traj[step_idx]

        active_node = _sub_to_node(step_idx, sub_frame)

        show_forces = (sub_frame == 0)
        show_vel    = (sub_frame == 1)
        show_disp   = (sub_frame == 2)
        prev_pos    = (self.traj[step_idx - 1]["positions"]
                       if step_idx > 0 else d["positions"])

        focus_atom = step_idx % 3

        self._draw_flowchart(self.ax_flow, active_node)
        self._draw_molecule_3d(
            self.ax_mol, d,
            focus_atom  = focus_atom,
            show_forces = show_forces,
            show_vel    = show_vel,
            show_disp   = show_disp,
            prev_pos    = prev_pos,
        )
        self._draw_descriptor(self.ax_desc, d, focus_atom)
        self._draw_accumulation(self.ax_accum, d)
        self._draw_gradient_force(self.ax_grad, d)
        self._draw_monitoring(self.ax_mon, step_idx)

        phases = {
            0: "Initializing          ",
            1: "Computing forces      ",
            2: "Velocity half-kick    ",
            3: "Updating positions    ",
            4: "Velocity half-kick    ",
        }
        phase_str = phases.get(active_node, "                      ")
        status = (
            f"Step {step_idx+1:02d}/{self.n_steps:02d}"
            f"  |  t = {d['time_fs']:5.1f} fs"
            f"  |  {phase_str}"
            f"  |  {self.model_label}"
        )
        self._status_txt.set_text(status)

        return []

    # -----------------------------------------------------------------------
    def run(self, output="part2c_deepmd.mp4", fps=FPS_C):
        print(f"\nRendering {self.total_frames} frames @ {fps} fps → {output}")
        print(f"  Duration: {self.total_frames / fps:.1f} s")
        ani = FuncAnimation(
            self.fig, self.animate,
            frames=self.total_frames,
            interval=1000 // fps,
            blit=False,
        )
        writer = FFMpegWriter(
            fps=fps, bitrate=2000,
            metadata={"title": "H2O DeePMD-kit MD"},
        )
        ani.save(output, writer=writer, dpi=130)
        print(f"Saved: {output}")
        plt.close(self.fig)


