def _sub_to_node(step_idx, sub_frame):
    if step_idx == 0 and sub_frame == 0:
        return 0
    if sub_frame == 0:
        return 1
    if sub_frame == 1:
        return 2
    if sub_frame == 2:
        return 3
    return 4


# ---------------------------------------------------------------------------
# Smooth cutoff (for descriptor inset visualization only)
# ---------------------------------------------------------------------------
def _f_cut(r_arr, r_cut=R_CUT, r_inner=R_INNER):
    r = np.asarray(r_arr, dtype=float)
    out = np.zeros_like(r)
    m_in  = r < r_inner
    m_mid = (r >= r_inner) & (r < r_cut)
    out[m_in] = 1.0
    u = (r[m_mid] - r_inner) / (r_cut - r_inner)
    out[m_mid] = u**3 * (-6*u**2 + 15*u - 10) + 1.0
    return out


# ---------------------------------------------------------------------------
# Sphere wireframe helper for 3D cutoff domain
# ---------------------------------------------------------------------------
def _sphere_wireframe(cx, cy, cz, r, n=12):
    """Return (xs, ys, zs) lists of line segments for a sphere wireframe."""
    lines = []
    # latitude circles
    for lat in np.linspace(-np.pi/2, np.pi/2, n//2):
        phi = np.linspace(0, 2*np.pi, 40)
        x = cx + r * np.cos(lat) * np.cos(phi)
        y = cy + r * np.cos(lat) * np.sin(phi)
        z = cz + r * np.sin(lat) * np.ones_like(phi)
        lines.append((x, y, z))
    # longitude circles
    for lon in np.linspace(0, np.pi, n//2):
        theta = np.linspace(0, 2*np.pi, 40)
        x = cx + r * np.sin(theta) * np.cos(lon)
        y = cy + r * np.sin(theta) * np.sin(lon)
        z = cz + r * np.cos(theta)
        lines.append((x, y, z))
    return lines


