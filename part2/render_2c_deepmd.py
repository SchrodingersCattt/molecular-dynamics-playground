"""
render_2c_deepmd.py — DeePMD-kit MD Animation for H₂O
=======================================================
Loads pre-computed trajectory from deepmd_data.npz (generated by
compute_2c_deepmd.py) and renders the animation.
Compute and render are fully decoupled:

    python part2/compute_2c_deepmd.py   # Bohrium run
    python part2/render_2c_deepmd.py    # render / re-render freely

Layout (2 rows × 3 cols, 16×9):
  top-left     → Abstract Velocity Verlet flowchart
  bottom-left  → 3-D molecule with cutoff sphere around focused atom
  top-mid      → Descriptor pipeline: s_ij bar chart + cutoff curve
  top-right    → Per-atom energy accumulation: ε_i stacking → E_total
  bottom-mid   → Gradient → Force: force magnitude bars per atom
  bottom-right → MD monitoring (KE / PE / TE + O-H bond lengths)

Output: part2c_deepmd.mp4
"""

